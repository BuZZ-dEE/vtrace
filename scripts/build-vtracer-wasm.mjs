import {existsSync, mkdirSync, readFileSync, rmSync, writeFileSync} from 'node:fs';
import {dirname, resolve} from 'node:path';
import {fileURLToPath} from 'node:url';
import {spawnSync} from 'node:child_process';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const crateManifest = resolve(root, 'rust/vtracer-wasm/Cargo.toml');
const generatedDir = resolve(root, '.generated/vtracer-wasm');
const wasmInput = resolve(
  root,
  'rust/vtracer-wasm/target/wasm32-unknown-unknown/release/vtrace_vtracer_wasm.wasm',
);
const jsOutput = resolve(generatedDir, 'vtracer.js');
const wasmOutput = resolve(generatedDir, 'vtracer_bg.wasm');
const tsOutput = resolve(root, 'src/vtracer-embedded.ts');

function run(command, args, options = {}) {
  const result = spawnSync(command, args, {
    cwd: root,
    stdio: 'inherit',
    shell: process.platform === 'win32',
    ...options,
  });

  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }
}

function findWasmBindgen() {
  if (process.env.WASM_BINDGEN) {
    return process.env.WASM_BINDGEN;
  }

  const result = spawnSync('wasm-bindgen', ['--version'], {
    cwd: root,
    stdio: 'ignore',
    shell: process.platform === 'win32',
  });

  return result.status === 0 ? 'wasm-bindgen' : null;
}

const wasmBindgen = findWasmBindgen();
if (!wasmBindgen) {
  console.error(
    'wasm-bindgen CLI is required. Install with: cargo install wasm-bindgen-cli --version 0.2.126 --locked',
  );
  process.exit(1);
}

rmSync(generatedDir, {recursive: true, force: true});
mkdirSync(generatedDir, {recursive: true});

run('cargo', [
  'build',
  '--manifest-path',
  crateManifest,
  '--release',
  '--target',
  'wasm32-unknown-unknown',
]);
run(wasmBindgen, [
  wasmInput,
  '--target',
  'web',
  '--out-dir',
  generatedDir,
  '--out-name',
  'vtracer',
]);

if (!existsSync(jsOutput) || !existsSync(wasmOutput)) {
  console.error('wasm-bindgen did not produce the expected vtracer.js and vtracer_bg.wasm files.');
  process.exit(1);
}

let generatedJs = readFileSync(jsOutput, 'utf8');
generatedJs = generatedJs.replace(
  "module_or_path = new URL('vtracer_bg.wasm', import.meta.url);",
  'module_or_path = decodeWasmBase64(VTRACER_WASM_BASE64);',
);

const wasmBase64 = readFileSync(wasmOutput).toString('base64');
const source = `// @ts-nocheck
/* tslint:disable */
/* eslint-disable */
// Generated by scripts/build-vtracer-wasm.mjs from rust/vtracer-wasm.
// The WASM payload is embedded so consumers do not need to serve a separate .wasm asset.

const VTRACER_WASM_BASE64 = ${JSON.stringify(wasmBase64)};

function decodeWasmBase64(base64) {
  if (typeof atob === 'function') {
    const binary = atob(base64);
    const bytes = new Uint8Array(binary.length);
    for (let i = 0; i < binary.length; i++) {
      bytes[i] = binary.charCodeAt(i);
    }
    return bytes;
  }

  return Uint8Array.from(Buffer.from(base64, 'base64'));
}

${generatedJs}

initSync(decodeWasmBase64(VTRACER_WASM_BASE64));
`;

writeFileSync(tsOutput, source);
